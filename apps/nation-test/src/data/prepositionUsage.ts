import type { FrequencyLevel } from '../types';

export type ArabicPreposition =
  | 'bi' | 'li' | 'fi' | 'min' | 'ila' | 'ala' | 'an' | 'maa' | 'hatta' | 'mundhu';

export interface PrepositionItem {
  id: string;
  level: FrequencyLevel;
  sentence: string;
  sentenceVocalized: string;
  translation: string;
  blankSentence: string;
  correctPreposition: ArabicPreposition;
  correctPrepositionArabic: string;
  distractors: {
    preposition: ArabicPreposition;
    arabic: string;
  }[];
  verb?: string;                    // The verb this preposition goes with
  verbMeaning?: string;
  explanation: string;
  collocationType: 'verb_prep' | 'noun_prep' | 'adj_prep' | 'fixed_expression';
}

