function enrichVerbType(items: WordDerivationItem[]): WordDerivationItem[] {
  return items.map(item =>
    item.verbType ? item : { ...item, verbType: detectVerbTypeFromRoot(item.root) }
  );
}

const allItems = [...wordDerivationItems, ...weakWordDerivationItems];

/**
 * Get word derivation items by frequency level
 */
export function getWordDerivationByLevel(levels: FrequencyLevel[]): WordDerivationItem[] {
  return enrichVerbType(allItems.filter((item) => levels.includes(item.level)));
}

/**
 * Get word derivation items by derivation type
 */
export function getWordDerivationByType(types: DerivationType[]): WordDerivationItem[] {
  return enrichVerbType(allItems.filter((item) => types.includes(item.derivationType)));
}

/**
 * Get all word derivation items
 */
export function getAllWordDerivationItems(): WordDerivationItem[] {
  return enrichVerbType(allItems);
}
