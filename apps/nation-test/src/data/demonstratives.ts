/**
 * Demonstratives Test Data
 *
 * Tests knowledge of Arabic demonstrative pronouns (أسماء الإشارة):
 * - Near: هذا، هذه، هذان، هاتان، هؤلاء
 * - Far: ذلك، تلك، ذانك، تانك، أولئك
 */

import type { FrequencyLevel } from '../types';

export type Demonstrative =
  | 'hadha' | 'hadhihi' | 'hadhani' | 'hatani' | 'haula'
  | 'dhalika' | 'tilka' | 'dhanika' | 'tanika' | 'ulaika';

export interface DemonstrativeItem {
  id: string;
  level: FrequencyLevel;
  sentence: string;
  sentenceVocalized: string;
  translation: string;
  blankSentence: string;
  correctDemonstrative: Demonstrative;
  correctDemonstrativeArabic: string;
  referent: string;                // What the demonstrative refers to
  referentGender: 'masculine' | 'feminine';
  referentNumber: 'singular' | 'dual' | 'plural';
  distance: 'near' | 'far';
  distractors: {
    demonstrative: Demonstrative;
    arabic: string;
  }[];
  explanation: string;
}

