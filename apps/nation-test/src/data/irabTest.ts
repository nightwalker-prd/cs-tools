/**
 * I'rab (Case Endings) Test Data
 *
 * Tests understanding of Arabic grammatical case endings:
 * - Nominative (مرفوع) - subject, predicate
 * - Accusative (منصوب) - object, adverbs
 * - Genitive (مجرور) - after prepositions, in idafa
 */

import type { FrequencyLevel } from '../types';

export type GrammaticalRole =
  | 'subject'           // فاعل
  | 'predicate'         // خبر
  | 'object'            // مفعول به
  | 'genitive'          // مجرور (after preposition)
  | 'idafa'             // مضاف إليه
  | 'adjective'         // صفة (follows noun case)
  | 'kana_predicate'    // خبر كان (accusative)
  | 'inna_subject'      // اسم إنّ (accusative)
  | 'hal'               // حال (accusative)
  | 'tamyiz';           // تمييز (accusative)

export type CaseEnding = 'nominative' | 'accusative' | 'genitive';

export interface IrabItem {
  id: string;
  level: FrequencyLevel;
  sentence: string;               // Sentence with target word (no diacritics on target)
  sentenceVocalized: string;      // Full sentence with correct vocalization
  targetWord: string;             // The word being tested (base form)
  correctAnswer: string;          // Word with correct case ending
  grammaticalRole: GrammaticalRole;
  caseEnding: CaseEnding;
  distractors: string[];          // Wrong case endings
  explanation: string;            // Why this case is used
  translation: string;
}

