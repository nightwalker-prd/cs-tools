import type { FrequencyLevel } from '../types';

export type MorphComponent = 'root' | 'pattern' | 'prefix' | 'suffix';

export interface MorphologicalAnalysisItem {
  id: string;
  level: FrequencyLevel;
  word: string;                    // The word to analyze (unvocalized)
  wordVocalized: string;           // With diacritics
  meaning: string;                 // English meaning
  root: string;                    // The root (e.g., ك-ت-ب)
  pattern: string;                 // The pattern in transliteration (e.g., فَاعِل)
  patternArabic: string;           // The pattern in Arabic script
  prefixes?: string[];             // Any prefixes (e.g., ال، و، ب)
  suffixes?: string[];             // Any suffixes (e.g., ون، ات، ة)
  questionType: MorphComponent;    // What component are we asking about?
  correctAnswer: string;           // The correct answer
  distractors: string[];           // Wrong options
  explanation: string;             // How to identify this component
}

