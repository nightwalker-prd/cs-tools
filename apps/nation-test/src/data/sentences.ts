// Merge expansion items into type-specific arrays
for (const item of sentenceExpansionItems) {
  sentencesByType[item.sentenceType]?.push(item);
}

/** Get all sentence test items */
export function getAllSentenceItems(): SentenceTestItem[] {
  return Object.values(sentencesByType).flat();
}

/** Get sentence items by types */
export function getSentenceItemsByTypes(types: SentenceType[]): SentenceTestItem[] {
  return types.flatMap((type) => sentencesByType[type] || []);
}

/** Get sentence items by level */
export function getSentenceItemsByLevel(level: FrequencyLevel): SentenceTestItem[] {
  return getAllSentenceItems().filter((item) => item.level === level);
}

