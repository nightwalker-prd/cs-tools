/**
 * Get translations by level
 */
export function getTranslationsByLevel(levels: FrequencyLevel[]): TranslationItem[] {
  return translations.filter((item) => levels.includes(item.level));
}

/**
 * Get all translations
 */
export function getAllTranslations(): TranslationItem[] {
  return translations;
}

/**
 * Generate distractor translations for multiple choice
 */
export function generateTranslationDistractors(
  correctItem: TranslationItem,
  allItems: TranslationItem[],
  count: number = 3
): string[] {
  const distractors = allItems
    .filter((item) => item.id !== correctItem.id)
    .map((item) => item.english);

  // Shuffle and pick
  const shuffled = distractors.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
