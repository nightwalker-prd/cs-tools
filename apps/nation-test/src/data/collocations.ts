/**
 * Arabic Collocation Test Data
 *
 * Tests knowledge of natural word combinations (collocations).
 * Based on the principle that vocabulary knowledge includes
 * knowing which words typically go together.
 */

import type { FrequencyLevel } from '../types';

export type CollocationType = 'verb_noun' | 'adj_noun' | 'verb_prep' | 'adv_verb';

export interface CollocationItem {
  id: string;
  /** The phrase with blank: "_____ خُطبة" */
  phraseWithBlank: string;
  /** Full phrase vocalized */
  fullPhraseVocalized: string;
  /** The correct collocate */
  correctCollocate: string;
  /** Correct collocate with diacritics */
  correctCollocateVocalized: string;
  /** Distractors that don't naturally combine */
  distractors: string[];
  /** English translation of the full phrase */
  translation: string;
  /** Collocation type for filtering */
  collocationType: CollocationType;
  /** Frequency level */
  level: FrequencyLevel;
}

