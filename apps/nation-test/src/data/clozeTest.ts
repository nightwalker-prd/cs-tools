/**
 * Get cloze items by frequency level
 */
export function getClozeItemsByLevel(levels: FrequencyLevel[]): ClozeItem[] {
  return clozeItems.filter((item) => levels.includes(item.level));
}

/**
 * Get cloze items by type
 */
export function getClozeItemsByType(types: ClozeType[]): ClozeItem[] {
  return clozeItems.filter((item) => types.includes(item.clozeType));
}

/**
 * Get all cloze items
 */
export function getAllClozeItems(): ClozeItem[] {
  return clozeItems;
}
