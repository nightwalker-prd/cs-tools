/**
 * Get reading comprehension items by level and topic
 */
export function getReadingComprehensionByLevelAndTopic(
  levels: FrequencyLevel[],
  topics?: PassageTopic[]
): ReadingComprehensionItem[] {
  let items = readingComprehensionData.filter(item => levels.includes(item.level));
  if (topics && topics.length > 0) {
    items = items.filter(item => topics.includes(item.topic));
  }
  return items;
}

/**
 * Remove diacritics from Arabic text for lenient matching
 */
export function removeDiacritics(text: string): string {
  return text.replace(/[\u064B-\u065F\u0670]/g, '');
}

/**
 * Check if a short answer is correct (lenient matching)
 */
export function isShortAnswerCorrect(
  userAnswer: string,
  correctAnswer: string,
  acceptableAnswers?: string[]
): boolean {
  const normalizedUser = removeDiacritics(userAnswer.toLowerCase().trim());
  const normalizedCorrect = removeDiacritics(correctAnswer.toLowerCase().trim());

  if (normalizedUser === normalizedCorrect) return true;
  if (normalizedCorrect.includes(normalizedUser) && normalizedUser.length > 3) return true;

  if (acceptableAnswers) {
    return acceptableAnswers.some(alt => {
      const normalizedAlt = removeDiacritics(alt.toLowerCase().trim());
      return normalizedUser === normalizedAlt ||
             (normalizedAlt.includes(normalizedUser) && normalizedUser.length > 3);
    });
  }

  return false;
}
