/**
 * Enrich items with computed verbType from root if not explicitly set
 */
function enrichVerbType(items: VerbConjugationItem[]): VerbConjugationItem[] {
  return items.map(item =>
    item.verbType ? item : { ...item, verbType: detectVerbTypeFromRoot(item.root) }
  );
}

/**
 * Get verb conjugation items by frequency level
 */
const allItems = [...verbConjugationItems, ...weakVerbConjugationItems];

export function getVerbConjugationByLevel(
  levels: FrequencyLevel[]
): VerbConjugationItem[] {
  return enrichVerbType(allItems.filter((item) => levels.includes(item.level)));
}

/**
 * Get verb conjugation items by tense
 */
export function getVerbConjugationByTense(
  tenses: VerbTense[]
): VerbConjugationItem[] {
  return allItems.filter((item) => tenses.includes(item.tense));
}

/**
 * Get verb conjugation items by form
 */
export function getVerbConjugationByForm(
  forms: VerbForm[]
): VerbConjugationItem[] {
  return allItems.filter((item) => forms.includes(item.form));
}

/**
 * Get all verb conjugation items
 */
export function getAllVerbConjugations(): VerbConjugationItem[] {
  return enrichVerbType(allItems);
}
