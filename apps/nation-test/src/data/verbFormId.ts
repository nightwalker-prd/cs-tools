export function getVerbFormIdByLevel(levels: FrequencyLevel[]): VerbFormIdItem[] {
  return enrichVerbType(allItems.filter(item => levels.includes(item.level)));
}

export function getVerbFormIdByForm(forms: ArabicVerbForm[]): VerbFormIdItem[] {
  return enrichVerbType(allItems.filter(item => forms.includes(item.correctForm)));
}

export function getAllVerbFormIdItems(): VerbFormIdItem[] {
  return enrichVerbType(allItems);
}
